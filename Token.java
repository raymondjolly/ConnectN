package com.rvj;

public class Token {
    private char token;

    /**
     * Constructor
     * @param token token
     */
    public Token(char token) {
        this.token = token;
    }

    /**
     * Getter of token
     * @return token
     */
    public char getToken() {
        return token;
    }

    /**
     * Setter of token
     * @param token token
     */
    public void setToken(char token) {
        this.token = token;
    }
}
